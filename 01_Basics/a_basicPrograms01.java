public class a_basicPrograms01 {
    public static void main(String[] args) {

        System.out.print("Learning DSA with Java");

        int yourNumber;
        yourNumber = 865;
        System.out.println(yourNumber);
        yourNumber = 87;
        System.out.println(yourNumber);

        char myCharacter;
        myCharacter = 'M';
        System.out.println(myCharacter);


        float myFloat = 5.0f;
        System.out.println(myFloat);

        boolean isVeg;
        isVeg = false;
        System.out.println(isVeg);

        String wishes = "Good Morning";
        System.out.println(wishes);

        //-->> \ this is an Escape Signal in Java
        System.out.println("Hello \"Abhay\"...");

        System.out.println("Hello \nAbhay...");

        System.out.println("Hello \tAbhay...");



    }
}



